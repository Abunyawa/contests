void solve() {
    int n;
    cin>>n;

    vector<pll> a(n);

    ll p = 0;
    for(int i=0;i<n;i++){
        cin>>a[i].fi>>a[i].se;
         
        if(a[i].fi<a[i].se){
            swap(a[i].fi,a[i].se);
        }

        p+=2LL*a[i].se;
    }

    sort(all(a));

    p+=a[n-1].fi*2LL;

    cout<<p<<'\n';
}

